// Helper function to get translation
export const getTranslation = (section, key, language = 'en') => {
  const sectionData = translations[section];
  if (!sectionData) return key;
  
  const langData = sectionData[language] || sectionData['en'];
  if (!langData) return key;
  
  return langData[key] !== undefined ? langData[key] : (sectionData['en']?.[key] || key);
};

// Get all translations for a section
export const getSectionTranslations = (section, language = 'en') => {
  const sectionData = translations[section];
  if (!sectionData) return {};
  
  return sectionData[language] || sectionData['en'] || {};
};
